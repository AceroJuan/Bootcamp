function addition (num1, num2){
    return num1+num2;
}

function substraction (num1, num2){
    return num1-num2;
}

function Multiplication (num1, num2){
    return num1*num2;
}

function Division (num1, num2){
    return num1/num2;
}

function Module (num1, num2){
    return num1%num2;
}

function RaisedBy (num1, num2){
    return Math.pow(num1,num2);
}